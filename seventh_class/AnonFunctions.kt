fun getOperation(symbol: Char):(Int,Int) -> Int {

    when(symbol){
        
        '+' -> return fun(num1:Int, num2:Int) = num1 + num2;
        '-' -> return fun(num1:Int, num2:Int) = num1 - num2;
        '*' -> return fun(num1:Int, num2:Int) = num1 * num2;
        '/' -> return fun(num1:Int, num2:Int): Int {
            if((num1 == 0) or (num2 == 0)){
                return 0;
            } else return num1 / num2;
        };
        else -> return fun(num1:Int, num2:Int) = 0;
    }
}

